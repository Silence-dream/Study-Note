public class Variable {
    public static void main(String[] args) {
        int a, b, c; // 声明整型变量
        int d = 4; // 声明变量并赋值
        System.out.println("d=" + d);
        byte e = 22;
        System.out.println("e=" + e);
        String s = "helloWorld";
        System.out.println("s=" + s);
        double pi = 3.14213;
        System.out.println("pi=" + pi);
        char x = 'd';
        System.out.println("x=" + x);
    }

}
